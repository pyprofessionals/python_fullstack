#Print Statement
'''
The print() function is used to display output on the display screen.
'''
print("Hello World")

#Printing multiple values
name="Yuvaraj"
age=25          
print(name,age)

#Printing with end parameter
print("Hello",end="\n")   # In print statement, end parameter is default move to newline character (\n)
print("World")

#Printing with sep parameter
print("Hello","World",sep="\n") # In print statement, sep parameter is default space character (\t)

#Printing Double Quotes inside a Output
print("name is 'Yuvaraj'")